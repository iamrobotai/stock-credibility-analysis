def regen_all():
    # 统计 LLM 覆盖
    llm_files = list(Path(DATADIR).glob("*_llm.json"))
    llm_ok = 0
    for f in llm_files:
        d = json.load(open(f, encoding="utf-8"))
        if d.get("success"):
            llm_ok += 1
    print(f"LLM 增强状态: {llm_ok} 成功 / {len(llm_files)} 总计")

    # 重新生成所有 docx
    ok, fail, skip = 0, 0, 0
    t0 = time.time()
    for ind_name, companies in INDUSTRIES:
        print(f"\n--- {ind_name} ---")
        for code, name in companies:
            scored_path = os.path.join(DATADIR, f"{code}_scored.json")
            if not os.path.exists(scored_path):
                print(f"  SKIP {name}_{code} (no scored data)")
                skip += 1
                continue
            try:
                generate(code, name, ind_name.split("_", 1)[-1])
                ok += 1
            except Exception as e:
                print(f"  FAIL {name}_{code}: {e}")
                fail += 1

    elapsed = time.time() - t0
    print(f"\n{'='*60}")
    print(f"重新生成完成: {ok} 成功 / {fail} 失败 / {skip} 跳过 | 耗时 {elapsed:.0f}s")

    # 打包 zip
    zip_path = os.path.join(BASE, "股票可信度分析_LLM增强版_2026-07-10.zip")
    with zipfile.ZipFile(zip_path, "w", zipfile.ZIP_DEFLATED) as z:
        for ind_name, companies in INDUSTRIES:
            for code, name in companies:
                safe = name.replace("/", "_").replace(" ", "")
                docx = os.path.join(BASE, f"{safe}_{code}_完整分析.docx")
                if os.path.isfile(docx):
                    z.write(docx, os.path.join(ind_name, os.path.basename(docx)))

    size_mb = os.path.getsize(zip_path) / 1e6
    print(f"\n打包完成: {zip_path} ({size_mb:.1f}MB)")
    with zipfile.ZipFile(zip_path) as z:
        print(f"总文件数: {len(z.namelist())}")

if __name__ == "__main__":
    regen_all()
