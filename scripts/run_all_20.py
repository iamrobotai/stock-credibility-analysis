def gen_config(ind):
    cfg = {"industry": ind["name"], "fmatrix": ind["F"], "companies": ind["companies"]}
    safe = ind["name"].replace("/", "_")
    path = os.path.join(BASE, f"config_full_{safe}.json")
    with open(path, "w", encoding="utf-8") as f:
        json.dump(cfg, f, ensure_ascii=False, indent=2)
    return path


def run_industry(ind_idx):
    ind = INDUSTRIES[ind_idx]
    cfg_path = gen_config(ind)
    print(f"\n{'#'*60}")
    print(f"# [{ind_idx+1}/20] {ind['name']} ({len(ind['companies'])} stocks)")
    print(f"{'#'*60}")
    result = subprocess.run([PY, "pipeline.py", cfg_path],
                            capture_output=True, text=True, encoding="utf-8",
                            cwd=BASE)
    # 打印关键行
    for line in result.stdout.split("\n"):
        if any(k in line for k in ["[docx]", "完成:", "耗时:", "FAIL", "[1/", "[2/", "[3/", "[4/", "[5/", "[6/", "[7/", "[8/", "[9/"]):
            print(line)
    return result.returncode == 0


